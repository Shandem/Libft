#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*n;
	int		i;

	n = malloc(sizeof(*s1));
	if (n == NULL)
		return (NULL);
	i = 0;
	while (s1[i] != '\0')
	{
		n[i] = s1[i];
		i++;
	}
	n[i] = '\0';
	return (n);
}
